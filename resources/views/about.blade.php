    <h1>What is CSRF?</h1>
    <p>Cross-Site Request Forgery (CSRF) is an attack that tricks the user into performing actions on a trusted site where they are authenticated.</p>
    <p>Laravel helps prevent these attacks using CSRF tokens. A CSRF token ensures the form request is coming from the intended user and not a malicious third party.</p>

